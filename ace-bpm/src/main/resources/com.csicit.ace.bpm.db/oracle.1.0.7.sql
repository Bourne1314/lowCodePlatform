COMMENT ON COLUMN "WFD_DELEGATE_USER"."ID" IS '主键';
COMMENT ON COLUMN "WFD_DELEGATE_USER"."REAL_NAME" IS '被委托人姓名';
COMMENT ON COLUMN "WFD_DELEGATE_USER"."RULE_ID" IS '委托规则主键';
