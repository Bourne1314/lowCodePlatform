COMMENT ON COLUMN "WFD_DELEGATE_RULE"."DELEGATE_USER_ID" IS '委托人主键';
COMMENT ON COLUMN "WFD_DELEGATE_RULE"."END_DATE" IS '有效期截止';
COMMENT ON COLUMN "WFD_DELEGATE_RULE"."ID" IS '主键';
COMMENT ON COLUMN "WFD_DELEGATE_RULE"."PROCESS_ID" IS '工作主键';
COMMENT ON COLUMN "WFD_DELEGATE_RULE"."PROCESS_NAME" IS '工作名称';
COMMENT ON COLUMN "WFD_DELEGATE_RULE"."START_DATE" IS '有效期起始';
COMMENT ON COLUMN "WFD_DELEGATE_RULE"."USED_FLAG" IS '是否启用，0否，1是';

